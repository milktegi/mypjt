<template>
	<div id="all-friends">
		<h2>all friends</h2>
		<div v-for="(friend, index) in friends" v-bind:key="index">
			<span @click="unfriend(friend.name)">{{ friend.name }}</span>
		</div>
	</div>
</template>


<script>
export default {
	
	name: 'AllFriends',
	props: ['friends'],
	data() {
		return {
		
		}
	},
	methods: {
		unfriend(name) {
			// 1. emit event 
			this.$emit('delete', { name: name })
		}
	}
}
</script>

